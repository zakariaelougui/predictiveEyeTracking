def preprocess_image(image_data):
   print("preprocess_image")

   if image_data.startswith('data:image'):
        # Find the start of the base64 string
      base64_str_idx = image_data.find('base64,') + 7
      # Extract the base64 string
      image_data = image_data[base64_str_idx:]

    # Decode the base64 string
   # Decode the base64 string
   image_bytes = base64.b64decode(image_data)
   print('image converted to bytes')
   # Create a file-like object from bytes
   image_file = BytesIO(image_bytes)
   print('image converted to file-like object')
   # Use Image.open to read the file-like object
   image = Image.open(image_file).convert('RGB')
   print('image converted to PIL image')
   image = np.array(image)
   print('image converted to numpy array')

   print('image shape:', image.shape)

   # Caricamento del centerbias preaddestrato
   centerbias_template = np.load('centerbias_mit1003.npy')
   # centerbias_template = np.zeros((1024, 1024))
   print("center bias loaded")

   # Rescaling per adattarsi alle dimensioni dell'immagine
   centerbias = zoom(centerbias_template, (image.shape[0]/centerbias_template.shape[0], image.shape[1]/centerbias_template.shape[1]), order=0, mode='nearest')
   
   # Rinormalizzazione del log density
   centerbias -= logsumexp(centerbias)
   
   # Conversione in tensori CUDA
   #image_tensor = torch.tensor([image.transpose(2, 0, 1)]).to(DEVICE) #serve a cuda
   #centerbias_tensor = torch.tensor([centerbias]).to(DEVICE)

   # Conversione in tensori CPU
   image_tensor = torch.tensor([image.transpose(2, 0, 1)], dtype=torch.float32) 
   centerbias_tensor = torch.tensor([centerbias], dtype=torch.float32)
   
   return image, image_tensor.to(DEVICE), centerbias_tensor.to(DEVICE)

def predict_fixation(image_tensor, centerbias_tensor):
   with torch.no_grad():
      log_density_prediction = model(image_tensor, centerbias_tensor)
   return log_density_prediction

def find_fixation_points(density_map, num_points=5):
    # Applica una leggera sfocatura alla mappa di densità per rendere i picchi più pronunciati
    smoothed_map = gaussian_filter(density_map, sigma=3)

    # Trova i massimi locali nella mappa di densità
    coordinates = peak_local_max(smoothed_map, num_peaks=num_points, min_distance=20)

    # Ordina i punti trovati per intensità della densità
    intensities = smoothed_map[coordinates[:, 0], coordinates[:, 1]]
    sorted_indices = np.argsort(intensities)[::-1]  # Ordinamento decrescente
    sorted_coords = coordinates[sorted_indices]

    return sorted_coords

